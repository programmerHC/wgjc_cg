package com.wgjc.param.entity;
/** 
 * @Description: 参数保存类
 * @author hc
 * @date 2019年9月10日下午12:03:35
 */
public class Param {
	private String code;
	private String desc;
	private String value;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
}
